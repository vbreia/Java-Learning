package hello_world;
public class java3 {

    // laços de repetição
    public static void main(String[] args) {

        // for
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }

        // while
        int j = 0;
        while (j < 10) {
            System.out.println(j);
            j++;
        }

        // do while
        int k = 0;
        do {
            System.out.println(k);
            k++;
        } while (k < 10);

    }
}
